ADD NUMBEROFGUESTS INTEGER;

CREATE TABLE SELLS(
	COFFEEHOUSE VARCHAR(20) REFERENCES COFFEEHOUSES(NAME),
	COFFEE VARCHAR(20) REFERENCES COFFEES(NAME),
	PRICE REAL,
	PRIMARY KEY (COFFEEHOUSE, COFFEE)
)